package arraysPro;

public class ForEachLoop {
  // It is used to iterate over the array elements.
  // Without using index values.
	// Syntex-- for(array datatype variablename : new arrayname)
	public static void main(String[] args) 
	{
		int arr[]= {3,5,3,6,8};
		for(int e:arr)
		{
			System.out.println(e);
		}

	}

}
